public class TodoThread extends Thread{
    public void run(){
        while (true){
            Calendar calendar=new GregorianCalendar();
            int hours = calendar.get(Calendar.HOUR_OF_DAY);
            MyFrame.getToDo().setForeground(Color.red);//设置字体颜色
            MyFrame.getToDo().setFont(new Font("宋体",Font.PLAIN,23));
            if (hours >= 8 && hours <= 9){
                MyFrame.getToDo().setText("消息：该上课了");

            }else if (hours >= 23 || hours <= 8){
                MyFrame.getToDo().setText("消息：该休息了");
            }else {
                MyFrame.getToDo().setText("消息：自由安排");
            }
            try {
                Thread.sleep(500);
            }catch (InterruptedException e){
                e.printStackTrace();
            }
        }
    }
}
